#!/usr/bin/env node
// One-off: turn public/pages/real/manifest.txt (pNNN.jpg|original name) into
// src/content/real-pages.js — the real Volume 28 page list, sectioned.
import { readFileSync, writeFileSync } from "node:fs";

const rows = readFileSync("public/pages/real/manifest.txt", "utf8")
  .trim().split(/\r?\n/).map((l) => {
    const [file, orig] = l.split("|");
    return { file, orig };
  });

// source page number (NaN for front matter)
const numOf = (orig) => { const m = orig.match(/^(\d+)_/); return m ? +m[1] : NaN; };
const titleOf = (orig) =>
  orig
    .replace(/^[0-9ivx]+_/i, "")
    .replace(/\.(png|jpg)(\.png)?$/i, "")
    .replace(/[_]+/g, " ")
    .trim();

// section ranges by SOURCE page number (front matter handled separately)
const SECTIONS = [
  { id: "messages", name: "Blessings & Messages", lang: "en", lo: 1, hi: 6,
    caption: "From our mentors",
    intro: "First, the hands that steady the lamp — words from those who guide PICT." },
  { id: "faculty", name: "Faculty & Departments", lang: "en", lo: 7, hi: 24,
    caption: "The departments",
    intro: "Every hall of learning, and the teachers who keep its flame." },
  { id: "batch", name: "Batch of 2026", lang: "en", lo: 25, hi: 42,
    caption: "The graduating batch",
    intro: "The seekers themselves — the Batch of Twenty Twenty-Six." },
  { id: "english", name: "English Section", lang: "en", lo: 43, hi: 70,
    caption: "English",
    intro: "Stories and essays in English — voices young and searching." },
  { id: "hindi", name: "हिन्दी विभाग", lang: "hi", lo: 71, hi: 84,
    caption: "हिन्दी",
    intro: "अब हिन्दी की गलियाँ — एहसास, कविताएँ और मुलाक़ातें।" },
  { id: "arts", name: "Arts & Photography", lang: "en", lo: 85, hi: 86,
    caption: "PICT arts",
    intro: "The eye behind the lens, the hand behind the brush." },
  { id: "marathi", name: "मराठी विभाग", lang: "mr", lo: 87, hi: 108,
    caption: "मराठी",
    intro: "आता मराठीचं दालन — कथा, कविता आणि गप्पा." },
  { id: "events", name: "Events & Celebrations", lang: "en", lo: 109, hi: 124,
    caption: "The year's events",
    intro: "The year as it was lived — festivals, contests and stage lights." },
  { id: "clubs", name: "Club Reports", lang: "en", lo: 125, hi: 999,
    caption: "Clubs of PICT",
    intro: "And finally, the many circles that make the campus turn." },
];

const tiers = [];
// front matter tier
const frontTitles = ["Saraswati Vandana", "The Makers — I", "The Makers — II",
  "In Remembrance", "Tributes", "The PICT Family", "Team Pictoreal", "Batch of 2026"];
tiers.push({
  id: "opening", name: "Opening Pages", section: "Opening Pages", lang: "en",
  caption: "Pictoreal · Vol. 28",
  intro: "Every volume opens with an invocation. Walk slowly here.",
  pages: rows.filter((r) => isNaN(numOf(r.orig))).map((r, i) => ({
    file: r.file, title: frontTitles[i] || titleOf(r.orig),
  })),
});
for (const s of SECTIONS) {
  tiers.push({
    id: s.id, name: s.name, section: s.name, lang: s.lang, caption: s.caption, intro: s.intro,
    pages: rows
      .filter((r) => { const n = numOf(r.orig); return n >= s.lo && n <= s.hi; })
      .map((r) => ({ file: r.file, title: titleOf(r.orig) })),
  });
}

let out = "// GENERATED by scripts/build-real-pages.mjs from public/pages/real/manifest.txt\n";
out += "// The real Pictoreal Vol. 28 draft pages, in magazine order.\n";
out += "export const realTiers = " + JSON.stringify(tiers, null, 2) + ";\n";
writeFileSync("src/content/real-pages.js", out);
const total = tiers.reduce((a, t) => a + t.pages.length, 0);
console.log("tiers:", tiers.map((t) => `${t.id}(${t.pages.length})`).join(" "), "| total:", total);
